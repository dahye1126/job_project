package model;

public class CorpSFBean {
	private int emp;
	private long sales;
	private long avg_wage;
	private int continuous_year;
	private int corp_code;
	private int year;
	
	public int getEmp() {
		return emp;
	}
	public void setEmp(int emp) {
		this.emp = emp;
	}
	public long getSales() {
		return sales;
	}
	public void setSales(long sales) {
		this.sales = sales;
	}
	public long getAvg_wage() {
		return avg_wage;
	}
	public void setAvg_wage(long avg_wage) {
		this.avg_wage = avg_wage;
	}
	public int getContinuous_year() {
		return continuous_year;
	}
	public void setContinuous_year(int continuous_year) {
		this.continuous_year = continuous_year;
	}
	public int getCorp_code() {
		return corp_code;
	}
	public void setCorp_code(int corp_code) {
		this.corp_code = corp_code;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	
}
